    '"שתי המסגרות חילוניות."',
    '"שגוי — שתי המסגרות פועלות על אותו רובד (אידיאולוגי-זהותי). ולטש מגיב לסינתזה הפוליטית של ויצמן באמצעות הצעה אונטולוגית."',
